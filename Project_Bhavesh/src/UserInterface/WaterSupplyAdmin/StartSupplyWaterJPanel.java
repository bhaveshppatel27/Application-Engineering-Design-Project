    private void buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonActionPerformed
        
        float b =0;
        float c = 0;
        int a = (int) volume.getValue();
        
        if(a<=0){
            JOptionPane.showMessageDialog(null, "Supply volume cannot be 0 or negative");
            return;
        }
        if(!(name.getText().matches("[a-zA-Z ]+$"))){
            JOptionPane.showMessageDialog(null, "Enter correct Sensor name");
            return;
        }
        if(location.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null , "Enter proper location");
            return;
        }
        try{
           b= Float.parseFloat(flow.getText());
           c= Float.parseFloat(pressure.getText());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Input Float Values only");
            return;
        }
        
        if(b<0){
            JOptionPane.showMessageDialog(null, "Flow rate cannot be negative");
            return;
        }
        if(c<=1){
            JOptionPane.showMessageDialog(null, "Pressure cannot be negative or 1");
            return;
        }
        if(b>a){
            JOptionPane.showMessageDialog(null, "Flow rate cannot be greter than supply volume");
            return;
        }
        SourceSensor sourceSensor = new SourceSensor();
        sourceSensor.setCustomer(customer);
        sourceSensor.setSensorName(name.getText());
        sourceSensor.setSupplyVolume(a);
        sourceSensor.setLocation("Mass Water Department,100 First St, Cambridge, MA 02141");
        sourceSensor.getSensorValue().setFlowrate(b);
        sourceSensor.getSensorValue().setPressure(c);
        
        
        customer.setSourceSensor(sourceSensor);
        customer.setVolumeAvailable(a);
        customer.getTargetSensor().setSensorName(name.getText());
        customer.getTargetSensor().setLocation(location.getText());
        
        SensorValue sensorValue = new SensorValue();
        sensorValue.setFlowrate(b);
        sensorValue.setPressure(c);
        sensorValue.setDate(new Date());
        customer.getTargetSensor().getSensorValueList().add(sensorValue);
        
        WaterSupplyAdmin waterSupplyAdmin = (WaterSupplyAdmin) userAccount.getEmployee();
        waterSupplyAdmin.getSensorList().add(sourceSensor);
        JOptionPane.showMessageDialog(null, "Supply Started for this customer");
        
        button.setEnabled(false);
        name.setEnabled(false);
        flow.setEnabled(false);
        pressure.setEnabled(false);
        volume.setEnabled(false);
        location.setEnabled(false);
        
    }//GEN-LAST:event_buttonActionPerformed


