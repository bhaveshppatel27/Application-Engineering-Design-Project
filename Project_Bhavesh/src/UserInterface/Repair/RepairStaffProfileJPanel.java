    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        RepairStaff rs = (RepairStaff) staff;
        int a = 0;
        try{
            a =Integer.parseInt(contactTxt.getText());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Enter Numbers only");
            return;
        }
        rs.setAvailability(String.valueOf(combo.getSelectedItem()));
        rs.setPhoneNumber(a);
        
        JOptionPane.showMessageDialog(null, "Added Successfully");
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        RepairStaff rs = (RepairStaff) staff;
        if(rs.getAvailability() == null){
            JOptionPane.showMessageDialog(null, "Add profile First");
            return;
        }
        container.remove(this);
        CardLayout layout = (CardLayout) container.getLayout();
        layout.previous(container);
        
    }//GEN-LAST:event_jButton2ActionPerformed


